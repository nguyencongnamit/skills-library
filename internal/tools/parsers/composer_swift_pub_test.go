package parsers

import "testing"

func TestParseComposerLock(t *testing.T) {
	body := []byte(`{
		"packages": [
			{"name": "monolog/monolog", "version": "2.9.1"},
			{"name": "guzzlehttp/guzzle", "version": "v7.5.0"}
		],
		"packages-dev": [
			{"name": "phpunit/phpunit", "version": "9.6.0"}
		]
	}`)
	deps, err := Parse("composer.lock", body)
	if err != nil {
		t.Fatalf("Parse(composer.lock): %v", err)
	}
	assertContains(t, deps,
		"monolog/monolog@2.9.1/composer",
		"guzzlehttp/guzzle@7.5.0/composer", // leading v stripped
		"phpunit/phpunit@9.6.0/composer",   // dev group included
	)
}

func TestParseSwiftPackageResolvedV2(t *testing.T) {
	body := []byte(`{
		"pins": [
			{"identity": "alamofire", "state": {"version": "5.8.1"}},
			{"identity": "swift-log", "state": {"version": "1.5.3"}},
			{"identity": "branch-pin", "state": {"branch": "main", "revision": "abc"}}
		],
		"version": 2
	}`)
	deps, err := Parse("Package.resolved", body)
	if err != nil {
		t.Fatalf("Parse(Package.resolved v2): %v", err)
	}
	assertContains(t, deps,
		"alamofire@5.8.1/swift",
		"swift-log@1.5.3/swift",
	)
	// A branch/revision pin has no released version -> skipped.
	for _, d := range deps {
		if d.Name == "branch-pin" {
			t.Errorf("branch pin should be skipped, got %+v", d)
		}
	}
}

